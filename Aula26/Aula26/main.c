#include <stdio.h>
#include <stdlib.h>

/*
    Aula 26
    Operador de resto da divisão
*/

int main()
{
    int a = 10, b = 20;

    int y = 20, w = 7;

    printf("\nSoma: %d\n", a + b);

    printf("Resto da divisão inteira %d\n", 10 % 20);

    printf("Resto da divisão inteira %d\n", b % a);
    printf("Resto da divisão inteira %d\n", y % w);



    return 0;
}
